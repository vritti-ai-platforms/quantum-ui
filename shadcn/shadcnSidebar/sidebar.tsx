type SidebarMode = 'expanded' | 'collapsed' | 'hover';

const SIDEBAR_MODE_OPTIONS: { value: SidebarMode; label: string }[] = [
  { value: 'expanded', label: 'Expanded' },
  { value: 'collapsed', label: 'Collapsed' },
  { value: 'hover', label: 'Expand on hover' },
];

// Manages sidebar mode state with localStorage persistence and hover behavior
function useSidebarMode(storageKey = 'sidebar-mode') {
  const { setOpen } = useSidebar();

  const [sidebarMode, setSidebarMode] = React.useState<SidebarMode>(() => {
    const stored = localStorage.getItem(storageKey);
    if (stored === 'expanded' || stored === 'collapsed' || stored === 'hover')
      return stored;
    return 'expanded';
  });

  // Stable ref to avoid re-triggering effect when setOpen identity changes
  const setOpenRef = React.useRef(setOpen);
  setOpenRef.current = setOpen;

  // Sync sidebar open/close when mode changes
  React.useEffect(() => {
    if (sidebarMode === 'expanded') {
      setOpenRef.current(true);
    } else {
      setOpenRef.current(false);
    }
  }, [sidebarMode]);

  function handleModeChange(mode: SidebarMode) {
    setSidebarMode(mode);
    localStorage.setItem(storageKey, mode);
  }

  function handleMouseEnter() {
    if (sidebarMode === 'hover') setOpen(true);
  }

  function handleMouseLeave() {
    if (sidebarMode === 'hover') setOpen(false);
  }

  return {
    sidebarMode,
    setSidebarMode: handleModeChange,
    sidebarProps: {
      overlay: sidebarMode === 'hover',
      onMouseEnter: handleMouseEnter,
      onMouseLeave: handleMouseLeave,
    },
    controlProps: {
      sidebarMode,
      onSidebarModeChange: handleModeChange,
    },
  };
}

